/**
 * Rewrites src/failureOriginTypes.generated.ts from workflow-truth-report.schema.json $defs.failureOrigin.enum
 */
import { readFileSync, writeFileSync } from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const schemaPath = path.join(root, "schemas", "workflow-truth-report.schema.json");
const outPath = path.join(root, "src", "failureOriginTypes.generated.ts");

const raw = readFileSync(schemaPath, "utf8");
const j = JSON.parse(raw);
const enumArr = j?.$defs?.failureOrigin?.enum;
if (!Array.isArray(enumArr) || enumArr.length === 0) {
  throw new Error("sync-failure-origin: missing $defs.failureOrigin.enum in workflow-truth-report.schema.json");
}
for (const v of enumArr) {
  if (typeof v !== "string") throw new Error("sync-failure-origin: enum values must be strings");
}

const lines = [
  "/** Generated by scripts/sync-failure-origin-from-schema.mjs — do not edit by hand. */",
  "",
  `export const FAILURE_ORIGINS = [`,
  ...enumArr.map((v) => `  ${JSON.stringify(v)},`),
  `] as const;`,
  "",
  `export type FailureOrigin = (typeof FAILURE_ORIGINS)[number];`,
  "",
];

writeFileSync(outPath, lines.join("\n"), "utf8");
console.log("sync-failure-origin: wrote", path.relative(root, outPath));
