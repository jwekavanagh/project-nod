#!/usr/bin/env node
/**
 * Sole codegen step for CLI commercial vs OSS profile.
 * Writes src/generated/commercialBuildFlags.ts (gitignored) before tsc.
 */
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.resolve(__dirname, "..");
const outDir = path.join(root, "src", "generated");
const outFile = path.join(outDir, "commercialBuildFlags.ts");

const profile = process.env.WF_BUILD_PROFILE ?? "oss";
const baseUrl =
  profile === "commercial"
    ? (process.env.COMMERCIAL_LICENSE_API_BASE_URL ?? "").trim()
    : "";

if (profile === "commercial" && !baseUrl) {
  console.error(
    "write-commercial-build-flags: WF_BUILD_PROFILE=commercial requires COMMERCIAL_LICENSE_API_BASE_URL",
  );
  process.exit(1);
}

const enabled = profile === "commercial";
const urlLiteral = JSON.stringify(baseUrl);

const body = `/* eslint-disable -- generated */
/** Generated by scripts/write-commercial-build-flags.mjs — do not edit. */
export const LICENSE_PREFLIGHT_ENABLED = ${enabled} as const;
export const LICENSE_API_BASE_URL = ${urlLiteral} as const;
`;

mkdirSync(outDir, { recursive: true });
writeFileSync(outFile, body, "utf8");
