/**
 * Embeds docs/first-run-integration.md and docs/partner-quickstart-commands.md into
 * website/src/generated/integratorDocsEmbedded.ts for serverless /integrate without repo fs.
 */
import { mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");
const outDir = join(root, "website", "src", "generated");
const firstRunPath = join(root, "docs", "first-run-integration.md");
const partnerPath = join(root, "docs", "partner-quickstart-commands.md");

function normalizeLf(raw) {
  return raw.replace(/\r\n/g, "\n").replace(/\r/g, "\n");
}

const firstRun = normalizeLf(readFileSync(firstRunPath, "utf8"));
const partner = normalizeLf(readFileSync(partnerPath, "utf8"));

mkdirSync(outDir, { recursive: true });

const header = `// AUTO-GENERATED by scripts/sync-integrator-docs-embedded.mjs — do not edit.

`;

const body = `${header}export const embeddedFirstRunIntegrationMd: string = ${JSON.stringify(firstRun)};

export const embeddedPartnerQuickstartCommandsMd: string = ${JSON.stringify(partner)};
`;

writeFileSync(join(outDir, "integratorDocsEmbedded.ts"), body, "utf8");
console.log("Wrote website/src/generated/integratorDocsEmbedded.ts");
